      public boolean verificaDadosPaciente(){
        if (nome.getText().isBlank() || nome.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campo "
                    + "nome é obrigatório e deve ser preenchido!", "Alerta",
                    ERROR_MESSAGE);  
            return false;
        }
        
        if (cpf.getText().isBlank() || cpf.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campo "
                    + "CPF é obrigatório e deve ser preenchido!", "Alerta", 
                    ERROR_MESSAGE);
            return false;
        }
        
        if (rg.getText().isBlank() || rg.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campo "
                    + "RG é obrigatório e deve ser preenchido!", "Alerta", 
                    ERROR_MESSAGE);
            return false;
        }
        
        if (emissor.getText().isBlank() || emissor.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campo "
                    + "Emissor é obrigatório e deve ser preenchido!", "Alerta", 
                    ERROR_MESSAGE);
            return false;
        }
        
        if (endereco.getText().isBlank() || endereco.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campo "
                    + "Endereço é obrigatório e deve ser preenchido!", "Alerta", 
                    ERROR_MESSAGE);
            return false;
        }
        
        if (numero.getText().isBlank() || numero.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campo "
                    + "RG é obrigatório e deve ser preenchido!", "Alerta", 
                    ERROR_MESSAGE);
            return false;
        }
        
        if (bairro.getText().isBlank() || bairro.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campo "
                    + "Bairro é obrigatório e deve ser preenchido!", "Alerta", 
                    ERROR_MESSAGE);
            return false;
        }
        
        if (cidade.getText().isBlank() || cidade.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campo "
                    + "Cidade é obrigatório e deve ser preenchido!", "Alerta", 
                    ERROR_MESSAGE);
            return false;
        }
        
        if ((telefone.getText().isBlank() || telefone.getText().isEmpty()) && (celular.getText().isEmpty() || celular.getText().isBlank())) {
            JOptionPane.showMessageDialog(null, "Campo "
                    + "Telefone ou Celular deve ser preenchido!", "Alerta", 
                    ERROR_MESSAGE);
            return false;
        }
        
        if (ano.getText().isBlank() || ano.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campo "
                    + "Dia do Nascimento é obrigatório e deve ser preenchido!", "Alerta", 
                    ERROR_MESSAGE);
            return false;
        }
        
        if (ano.getText().isBlank() || ano.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campo "
                    + "Mês do Nascimento é obrigatório e deve ser preenchido!", "Alerta", 
                    ERROR_MESSAGE);
            return false;
        }
        
        if (ano.getText().isBlank() || ano.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campo "
                    + "Ano do Nascimento é obrigatório e deve ser preenchido!", "Alerta", 
                    ERROR_MESSAGE);
            return false;
        }
        
        if (empresa.getText().isBlank() && !possuiConvenio.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Digite a empresa "
                    + "do convênio.", "Alerta", 
                    ERROR_MESSAGE);
            return false;
        }
        return true;
    }
  
