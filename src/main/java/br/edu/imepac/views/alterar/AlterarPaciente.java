    private void cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarActionPerformed
        
        if(verificaDadosPaciente()){
            paciente.setNomeCompleto(nome.getText());
            paciente.setNumeroRg(rg.getText());
            paciente.setOrgaoEmissor(emissor.getText());
            paciente.setNumeroCpf(cpf.getText());            
            paciente.setEndereco(endereco.getText());
            paciente.setNumero(numero.getText());
            if(complemento.getText().isBlank() || complemento.getText().isEmpty()){
                paciente.setComplemento("");
            }else{
                paciente.setComplemento(complemento.getText());
            }
            paciente.setBairro(bairro.getText());
            paciente.setCidade(cidade.getText());
            String getEstado = estado.getSelectedItem().toString();
            paciente.setEstado(getEstado);
            if (telefone.getText().isBlank() || telefone.getText().isEmpty()){
               paciente.setCelular(celular.getText());
               paciente.setTelefone("");
            }else if(celular.getText().isBlank() || celular.getText().isEmpty()){
                paciente.setTelefone(telefone.getText());
                paciente.setCelular("");
            }else{
                 paciente.setTelefone(telefone.getText());
                 paciente.setCelular(celular.getText());
            }
            
            String getSexo = sexo.getSelectedItem().toString();
            if(getSexo.equals("Masculino")){
                paciente.setSexo("M");
            }else if(getSexo.equals("Feminino")){
                paciente.setSexo("F");
            }
            paciente.setDataNascimento(diaNascimento.getText(), mesNascimento.getText(), ano.getText());
            if(possuiConvenio.isSelected()){
                paciente.setTemConvenio("S");
                convenio.setNomeConvenio(empresa.getText());
            }else{
                paciente.setTemConvenio("N");
            }
            paciente.setSenhaAcesso(cpf.getText());
            
             try{
                ConexaoBancoDeDados conexao = new ConexaoBancoDeDados();
                Pacientes pac = new Pacientes();
                
                if(conexao.abrirConexao()){
                    pac.configurarConexao(conexao.obterConexao());
                    if(pac.inserirRegistro(paciente)){                                    
                        JOptionPane.showMessageDialog(null, "Paciente "
                         + paciente.getNomeCompleto()+ " cadastrado com sucesso."); 
                    }else{
                        JOptionPane.showMessageDialog(null, ""
                        + "Não foi possivel cadastrar o paciente", "Alerta", 
                         ERROR_MESSAGE);
                    }
                    conexao.fecharConexao();
                }else{
                        JOptionPane.showMessageDialog(null, ""
                        + "Não foi possivel estabelecer conexão com o banco de dados!"
                        , "Alerta", ERROR_MESSAGE);
                }
            }catch(Exception erro){
                erro.printStackTrace();
                JOptionPane.showMessageDialog(null, ""
                + "Erro do sistema: processo de cadastro de paciente"
                , "Alerta", ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_cadastrarActionPerformed

